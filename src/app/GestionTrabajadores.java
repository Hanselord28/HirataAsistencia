    private void btnEliminarTrabajadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarTrabajadorActionPerformed
        try {
            // Tomar el id seleccionado del ComboBox
            int idUsuario = Integer.parseInt(cbbBorrarIdTrabajador.getSelectedItem().toString());

            // Confirmación antes de eliminar
            int confirm = JOptionPane.showConfirmDialog(this,
                    "¿Seguro que deseas eliminar al usuario con ID: " + idUsuario + "?",
                    "Confirmar eliminación",
                    JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                DAO_Usuarios dao = new DAO_Usuarios();
                dao.eliminarUsuario(idUsuario);

                JOptionPane.showMessageDialog(this, "Usuario eliminado correctamente.");

                // Refrescar ComboBox después de eliminar
                cbbBorrarIdTrabajador.removeItem(cbbBorrarIdTrabajador.getSelectedItem());
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al eliminar: " + ex.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Selecciona un usuario válido.");
        }
    }//GEN-LAST:event_btnEliminarTrabajadorActionPerformed

    private void btnAgregarUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarUpdateActionPerformed
        try {
            int idSeleccionado = Integer.parseInt(cbbidTrabajador.getSelectedItem().toString());
            Usuario usuario = new Usuario();
            usuario.setIdUsuario(idSeleccionado);
            usuario.setNombreCompleto(txtModificarNombreTrabajador.getText());
            usuario.setCorreo(txtModificarCorreoTrabajador.getText());
            usuario.setContrasena(txtModificarContrasenaTrabajador.getText());
            usuario.setRol(cbbModificarRol.getSelectedItem().toString());

            // Fecha
            java.util.Date fecha = java.sql.Date.valueOf(txtModificarFechaCreacion.getText()); // formato yyyy-MM-dd
            usuario.setFechaCreacion(fecha);

            DAO_Usuarios dao = new DAO_Usuarios();
            dao.modificarUsuario(usuario);
            cargarComboIds();

            JOptionPane.showMessageDialog(this, "Usuario modificado correctamente.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al modificar usuario: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnAgregarUpdateActionPerformed

