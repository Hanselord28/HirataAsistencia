    private void btnReporteGeneralActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReporteGeneralActionPerformed
        try {
            // 1️⃣ Instanciamos el DAO de Asistencias
            DAO_Asistencia dao = new DAO_Asistencia();

            // 2️⃣ Obtenemos todas las asistencias (Reporte General)
            List<Asistencia> lista = dao.obtenerReporteGeneral();

            // 3️⃣ Instanciamos el generador de PDF
            ReporteAsistenciaPDF generador = new ReporteAsistenciaPDF();

            // 4️⃣ Título del reporte
            String titulo = "Reporte General de Asistencias";

            // 5️⃣ Generar PDF
            generador.generarReporte(lista, titulo);

            // 6️⃣ Mensaje de confirmación
            javax.swing.JOptionPane.showMessageDialog(null, "✅ Reporte General generado correctamente en Descargas.");

        } catch (Exception ex) {
            ex.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(null, "❌ Error al generar el reporte: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnReporteGeneralActionPerformed

