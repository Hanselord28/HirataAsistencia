    private void cargarComboIds() {
        try {
            DAO_Usuarios dao = new DAO_Usuarios();
            List<Integer> ids = dao.obtenerIdsUsuarios();
            cbbidTrabajador.removeAllItems();
            cbbBorrarIdTrabajador.removeAllItems();
            for (int id : ids) {
                cbbidTrabajador.addItem(String.valueOf(id));
                cbbBorrarIdTrabajador.addItem(String.valueOf(id));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al cargar IDs: " + ex.getMessage());
        }
    }

