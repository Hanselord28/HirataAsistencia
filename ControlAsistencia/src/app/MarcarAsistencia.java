    private void jbtnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnEntrarActionPerformed
        try {
            DAO_Asistencia dao = new DAO_Asistencia();
            Asistencia asistencia = dao.obtenerAsistenciaHoy(usuarioLogueado.getIdUsuario());

            if (asistencia == null || asistencia.getHoraEntrada() == null) {
                JOptionPane.showMessageDialog(this, "Debes marcar entrada primero.");
            } else if (asistencia.getHoraSalida() != null) {
                JOptionPane.showMessageDialog(this, "Ya marcaste tu salida hoy.");
            } else {
                dao.marcarSalida(asistencia.getIdAsistencia());
                JOptionPane.showMessageDialog(this, "Salida registrada correctamente.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al marcar salida: " + e.getMessage());
        }
    }//GEN-LAST:event_jbtnEntrarActionPerformed

    private void jbtnEntrar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnEntrar1ActionPerformed
        try {
            DAO_Asistencia dao = new DAO_Asistencia();
            Asistencia asistencia = dao.obtenerAsistenciaHoy(usuarioLogueado.getIdUsuario());

            if (asistencia == null) {
                dao.marcarEntrada(usuarioLogueado.getIdUsuario());
                JOptionPane.showMessageDialog(this, "Entrada registrada correctamente.");
            } else if (asistencia.getHoraEntrada() != null) {
                JOptionPane.showMessageDialog(this, "Ya marcaste tu entrada hoy.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al marcar entrada: " + e.getMessage());
        }
    }//GEN-LAST:event_jbtnEntrar1ActionPerformed

