    private void btnAgregarUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarUpdateActionPerformed
        try {
            int idSeleccionado = Integer.parseInt(cbbidTrabajador.getSelectedItem().toString());
            Usuario usuario = new Usuario();
            usuario.setIdUsuario(idSeleccionado);
            usuario.setNombreCompleto(txtModificarNombreTrabajador.getText());
            usuario.setCorreo(txtModificarCorreoTrabajador.getText());
            usuario.setContrasena(txtModificarContrasenaTrabajador.getText());
            usuario.setRol(cbbModificarRol.getSelectedItem().toString());

            // Fecha
            java.util.Date fecha = java.sql.Date.valueOf(txtModificarFechaCreacion.getText()); // formato yyyy-MM-dd
            usuario.setFechaCreacion(fecha);

            DAO_Usuarios dao = new DAO_Usuarios();
            dao.modificarUsuario(usuario);
            cargarComboIds();

            JOptionPane.showMessageDialog(this, "Usuario modificado correctamente.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al modificar usuario: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnAgregarUpdateActionPerformed

