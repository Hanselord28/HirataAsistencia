    private void btnAgregarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarRegistroActionPerformed
        try {
            Usuario nuevoUsuario = new Usuario();
            nuevoUsuario.setNombreCompleto(txtNombreTrabajador.getText());
            nuevoUsuario.setCorreo(txtCorreoTrabajador.getText());
            nuevoUsuario.setContrasena(txtContraseñaTrabajador.getText());
            nuevoUsuario.setRol(cbbRolTrabajador.getSelectedItem().toString());

            // Si usas JTextField para fecha
            java.util.Date fecha = java.sql.Date.valueOf(txtFechaCreacion.getText()); // formato: yyyy-MM-dd
            nuevoUsuario.setFechaCreacion(fecha);

            DAO_Usuarios dao = new DAO_Usuarios();
            dao.agregarUsuario(nuevoUsuario);

            JOptionPane.showMessageDialog(this, "Usuario agregado correctamente.");
            btnCancelarRegistroActionPerformed(null); // Limpia los campos
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al agregar usuario: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnAgregarRegistroActionPerformed

